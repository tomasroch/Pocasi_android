package com.pocasi_android.model;

public class Wind {
    private Double speed;
    private Integer deg;
    public Wind() {    }
    public Double getSpeed() {
        return speed;
    }

    public void setSpeed(Double speed) {
        this.speed = speed;
    }

    public Integer getDeg() {
        return deg;
    }

    public void setDeg(Integer deg) {
        this.deg = deg;
    }
}
